#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t size)
{
	while ((*s1 || *s2) && size-- > 0)
		if (*(s1++) != *(s2++))
			return (*(s1 - 1) - *(s2 - 1));
	return (0);
}
